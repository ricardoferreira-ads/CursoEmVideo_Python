'''Faça um programa que leia um número e mostre seu dobro, seu tripulo e a raiz quadrada'''
numero = int(input('Digite um número: '))
dobro = numero * 2
trip = numero * 3
raizQuad = numero ** 1/2
print('O dobro do número digitqado é: {}.\nO tripulo do número digitado é: {}\n. A raiz quadrada do número digitado é: {}'.format(dobro, trip, raizQuad))
